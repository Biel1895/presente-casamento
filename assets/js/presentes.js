function abrirCard1() {
    let caixa1 = document.getElementById('caixa1');
    let card1 = document.getElementById('card1');

    if (card1.classList.contains('d-none')) {
        card1.classList.remove('d-none');
        caixa1.classList.add('d-none');
    }
}

